def ninety_nine num_of_bottles

while num_of_bottles > 2

puts " #{num_of_bottles}  bottles of beer on the wall, #{num_of_bottles}  bottles of beer ."

num_of_bottles -= 1

puts "Take one down, pass it around, #{num_of_bottles} bottles of beer on the wall..."

end

puts "1 bottle of beer on the wall, 1 bottle of beer...

  Take one down, pass it around, no more bottles of beer on the wall..."

puts "\n     Go to the store and buy some more..."
end

ninety_nine 9999
